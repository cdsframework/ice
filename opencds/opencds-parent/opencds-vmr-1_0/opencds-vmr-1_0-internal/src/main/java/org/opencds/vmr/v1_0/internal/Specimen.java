package org.opencds.vmr.v1_0.internal;

import java.util.Arrays;



/*
 * A sample of tissue, blood, urine, water, air, etc., taken for the purposes of diagnostic examination or evaluation.
 */

public class Specimen extends EntityBase
{
	protected String description;

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result
				+ ((description == null) ? 0 : description.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		Specimen other = (Specimen) obj;
		if (description == null) {
			if (other.description != null)
				return false;
		} else if (!description.equals(other.description))
			return false;
		return true;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Specimen [description=" + description + ", templateId="
				+ Arrays.toString(templateId) + ", id=" + id + ", entityType="
				+ entityType + ", evaluatedPersonId=" + evaluatedPersonId
				+ ", toBeReturned=" + toBeReturned + ", relationshipToSources="
				+ relationshipToSources + "]";
	}

}
